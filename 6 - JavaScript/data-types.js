//Data Types - Primitive data types

//Number: Represents numeric va;ue e.g. -12, 45, 1.5
let age = 25;
console.log("The value of age is:", age);
console.log("The type of age is:", typeof(age));


//Strings: Represents textual values enclosed in '' or "" e.g. "abc123!@#"
let name = 'John';
console.log("The value of name is:", name);
console.log("The type of name is:", typeof(name));

//Boolean: Either true or false
let isAdmin = true;
isAdmin = false;
console.log("The value of isAdmin is:", isAdmin);
console.log("The type of isAdmin is:", typeof(isAdmin));


//Null: Represents intentional absence of value
let car = null;
console.log("The value of car is:", car);
console.log("The type of car is:", typeof(car));


//Undefined: Represents unintentional absence of value
let city;
console.log("The value of city is:", city);
console.log("The type of city is:", typeof(city));